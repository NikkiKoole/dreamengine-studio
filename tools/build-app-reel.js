#!/usr/bin/env node
// build-app-reel.js — generate an APP TRAILER (showreel) from a manifest: apps/<name>/app.json
// carts[] → one clip per rack (bake its committed recipe if not already baked) → a generated
// tools/reels/<name>.reel → compose-clips → editor/public/reels/<name>.webm. The "app trailer"
// pipeline: the multi-cart unit of docs/design/demand-generation.md #4, built on transitions.md's
// Layer B (compose-clips). The generated .reel is committed + hand-editable — reorder its lines or
// change a `| transition secs` to art-direct the cut, then re-run (or use the editor picker).
//
//   node tools/build-app-reel.js tinyjam
//   node tools/build-app-reel.js tinyjam --xfade dissolve 0.6   # default transition + seconds
//   node tools/build-app-reel.js tinyjam --no-compose           # just (re)write the .reel
//   node tools/build-app-reel.js tinyjam --json                 # machine-readable result
//
// A rack with no committed clip is SKIPPED (and reported) — capture one first:
//   node tools/play.js <cart> record demo.rec   (you drive) → park at tools/clips/<cart>/01-x.rec
// so the trailer is only ever as complete as the clips that exist (no silent gaps).
'use strict'

const fs = require('fs')
const path = require('path')
const { spawnSync } = require('child_process')
const ROOT = path.join(__dirname, '..')

const USAGE = 'usage: node tools/build-app-reel.js <app> [--xfade type secs] [--no-compose] [--json]'
function die(m) { console.error(m + '\n' + USAGE); process.exit(1) }

const argv = process.argv.slice(2)
let app = null, xtype = 'fade', xdur = 0.5, noCompose = false, asJson = false
for (let i = 0; i < argv.length; i++) {
  const a = argv[i]
  if (a === '--xfade') { xtype = argv[++i] || 'fade'; xdur = parseFloat(argv[++i]) || 0.5 }
  else if (a === '--no-compose') noCompose = true
  else if (a === '--json') asJson = true
  else if (a.startsWith('-')) die(`unknown flag: ${a}`)
  else if (!app) app = a
  else die(`unexpected arg: ${a}`)
}
if (!app) die('which app? e.g. node tools/build-app-reel.js tinyjam')

const manifestPath = path.join(ROOT, 'apps', app, 'app.json')
if (!fs.existsSync(manifestPath)) die(`no manifest at apps/${app}/app.json`)
const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'))
const carts = manifest.carts || []
if (!carts.length) die(`apps/${app}/app.json has no carts[]`)

const log = m => { if (!asJson) process.stderr.write(m + '\n') }

// Find a clip label for a cart: prefer an already-baked editor/public/clips/<cart>/*.webm; else
// bake the first committed recipe (tools/clips/<cart>/*.{script,beats,rec}) via make-gif; else null.
function clipFor(cart) {
  const pubDir = path.join(ROOT, 'editor/public/clips', cart)
  const baked = fs.existsSync(pubDir) ? fs.readdirSync(pubDir).filter(f => f.endsWith('.webm')).sort() : []
  if (baked.length) return { label: baked[0].replace(/\.webm$/, ''), baked: true }
  const recDir = path.join(ROOT, 'tools/clips', cart)
  const recs = fs.existsSync(recDir) ? fs.readdirSync(recDir).filter(f => /\.(script|beats|rec)$/.test(f)).sort() : []
  if (!recs.length) return null
  const label = recs[0].replace(/\.(script|beats|rec)$/, '')
  log(`  baking ${cart}/${label} …`)
  const r = spawnSync('node', [path.join(ROOT, 'tools/make-gif.js'), cart, '--recipe', label],
    { cwd: ROOT, stdio: asJson ? 'ignore' : 'inherit' })
  if (r.status === 0 && fs.existsSync(path.join(pubDir, `${label}.webm`))) return { label, baked: false }
  log(`  ✗ bake failed for ${cart}`)
  return null
}

log(`build-app-reel: ${manifest.name || app} — racks: ${carts.join(', ')}`)
const included = [], skipped = []
for (const cart of carts) {
  const c = clipFor(cart)
  if (c) included.push(`${cart}/${c.label}`)
  else { skipped.push(cart); log(`  · skip ${cart} — no clip (record one → tools/clips/${cart}/)`) }
}
if (!included.length) die(`no clips for any rack in "${app}" — capture at least one (see the header)`)

// write the generated .reel (committed + hand-editable)
const reelPath = path.join(ROOT, 'tools/reels', `${app}.reel`)
let reel = `# ${manifest.name || app} — app trailer (generated by build-app-reel.js; edit freely)\n`
reel += `# fps 30\n# xfade ${xtype} ${xdur}\n`
reel += included.join('\n') + '\n'
fs.mkdirSync(path.dirname(reelPath), { recursive: true })
fs.writeFileSync(reelPath, reel)
log(`\n✓ wrote tools/reels/${app}.reel — ${included.length} rack(s)` + (skipped.length ? `, skipped: ${skipped.join(', ')}` : ''))

let out = null
if (!noCompose && included.length >= 2) {
  log(`composing → editor/public/reels/${app}.webm …`)
  const r = spawnSync('node', [path.join(ROOT, 'tools/compose-clips.js'), app], { cwd: ROOT, stdio: asJson ? 'ignore' : 'inherit' })
  if (r.status === 0) { out = `editor/public/reels/${app}.webm`; log(`✓ ${out}`) }
  else log(`✗ compose failed`)
} else if (included.length < 2) {
  log(`(only ${included.length} clip — need ≥2 to compose a reel with transitions; capture more racks)`)
}

if (asJson) console.log(JSON.stringify({ app, reel: `tools/reels/${app}.reel`, included, skipped, out }, null, 2))
