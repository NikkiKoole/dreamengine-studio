#!/usr/bin/env node
// Generate runtime/studio_tcc_symbols.h — an X-macro list of every studio.h API
// function, so the -DDE_TCC host can expose them all to a libtcc-loaded cart via
// tcc_add_symbol. This is the auto-derived "5th place" (alongside the four-places
// rule in CLAUDE.md): re-run it whenever studio.h's declarations change.
//
//   node tools/gen-tcc-symbols.js
//
// It parses single-line prototypes `<rettype> name(args);` and emits the bare names.
// The three cart entry points (init/update/draw) are NOT host symbols — the cart
// defines them and the host fetches them via tcc_get_symbol — so they're excluded.

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const HDR  = path.join(ROOT, 'runtime', 'studio.h');
const OUT  = path.join(ROOT, 'runtime', 'studio_tcc_symbols.h');

const ENTRY_POINTS = new Set(['init', 'update', 'draw']);

const src = fs.readFileSync(HDR, 'utf8');
const names = [];
const seen = new Set();
for (const line of src.split('\n')) {
    if (!/\);\s*(\/\/.*)?$/.test(line)) continue;        // must be a prototype line
    const m = line.match(/^[A-Za-z_].*?\b([A-Za-z_]\w*)\s*\(/); // name right before '('
    if (!m) continue;
    const n = m[1];
    if (ENTRY_POINTS.has(n) || seen.has(n)) continue;
    seen.add(n);
    names.push(n);
}

const body = names.map(n => `    X(${n})`).join(' \\\n');
const out =
`// AUTO-GENERATED by tools/gen-tcc-symbols.js from studio.h — DO NOT EDIT.
// X-macro list of studio API functions exposed to libtcc-loaded carts (-DDE_TCC).
// ${names.length} symbols. Regenerate after changing studio.h's declarations.
#define DE_TCC_API_SYMBOLS(X) \\
${body}
`;

fs.writeFileSync(OUT, out);
console.log(`wrote ${OUT} with ${names.length} symbols`);
